package Org.Example.No4;

public class CountDownFrom10 {
    public static void main(String[] args) {

        //Virker ved at lave en int n og sætte den til 10
        // Efterfølgende skriver vi at n må være større eller lig med 1
        // og n -- får forløkken til at tælle nedad

        for (int n=10; n>=1; n --){
            System.out.println(n);
        }
    }

}
