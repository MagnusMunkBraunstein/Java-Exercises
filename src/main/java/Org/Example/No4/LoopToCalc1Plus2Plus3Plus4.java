package Org.Example.No4;

// calculate 1+2+3+4+5+6..+20. by using a for loop

public class LoopToCalc1Plus2Plus3Plus4 {

    public static void main(String[] args) {

        int sum = 0;
        for (int i = 1; i<= 20; i++){
            sum +=i;
        }

        System.out.println("The sum is " + sum);






    }

}
